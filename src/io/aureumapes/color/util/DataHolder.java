package io.aureumapes.color.util;


public class DataHolder {
    public int x, y, width, height;

    public DataHolder() {
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
